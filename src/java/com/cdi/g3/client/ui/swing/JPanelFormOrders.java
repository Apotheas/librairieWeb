    private void selectLineTableOrdersActionPerformed(ListSelectionEvent evt) {
        final String mname = "selectLineTableOrdersActionPerformed";

        if (evt.getValueIsAdjusting()) {
            return;
        }

        ListSelectionModel lsm = (ListSelectionModel) evt.getSource();

        if (lsm.isSelectionEmpty()) {
//            JOptionPane.showMessageDialog(this, "No rows selected", "Warning", JOptionPane.WARNING_MESSAGE);
//           
            System.out.println("No rows selected");
        } else {
            int selectedRow = lsm.getMinSelectionIndex(); 
            indiceSelectedTableOrders=selectedRow;
            Orders order = (Orders) Ordersvector.get(selectedRow);
            
            if (order.getDatepachageShipperOrder()!= null)
                jTextFieldDateShipping.setText(df.format(order.getDatepachageShipperOrder()));
            else 
                jTextFieldDateShipping.setText(""); 
            
            InfoStatus infostatus  = null;
            try {
              infostatus =  infostatusService.findInfoStatus(order.getNameInfoStatus().getId());
            } catch (FinderException ex) {
                Logger.getLogger(JPanelFormOrders.class.getName()).log(Level.SEVERE, null, ex);
            } catch (CheckException ex) {
                Logger.getLogger(JPanelFormOrders.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jComboBoxOrderStatusUpdate.setSelectedIndex(infostatus.getValueInfoStatus()-1);
            
             if (order.getPachageShipper()!= null){
                PachageShipper  pachageShipper  = null;
                try {
                 pachageShipper =  serviceOrder.findPackagesShipper(order.getPachageShipper().getId());
                } catch (FinderException ex) {
                Logger.getLogger(JPanelFormOrders.class.getName()).log(Level.SEVERE, null, ex);
                } 
                 jComboBoxPackageChipper.setSelectedIndex(Integer.parseInt(pachageShipper.getId()));
           
             }
                 else {
                  jComboBoxPackageChipper.setSelectedItem(0);
                  chargeComboBoxPackageShipperOrders();
             }
            OrderLinesvector.clear();
            OrderLinesvector.addAll(order.getListOrderLines());
            jTableOrdersLine.setModel(initOrdersLineTableModel(OrderLinesvector));
        }
    }

